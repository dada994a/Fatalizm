    public static AxisAlignedBB generateBB(BlockPos blockPos) {
        return new AxisAlignedBB
                (
                        blockPos.getX() - Minecraft.getMinecraft().getRenderManager().viewerPosX,
                        blockPos.getY() - Minecraft.getMinecraft().getRenderManager().viewerPosY,
                        blockPos.getZ() - Minecraft.getMinecraft().getRenderManager().viewerPosZ,
                        blockPos.getX() + 1 - Minecraft.getMinecraft().getRenderManager().viewerPosX,
                        blockPos.getY() + 1 - Minecraft.getMinecraft().getRenderManager().viewerPosY,
                        blockPos.getZ() + 1 - Minecraft.getMinecraft().getRenderManager().viewerPosZ
                );
    }
    public static void drawBlock(BlockPos pos, Color fillColor, Color outLine, float lineWidth, RenderMode mode) {
        TessellatorUtil.prepare();
        switch (mode) {
            case Fill:
                drawFill(generateBB(pos), fillColor);
                break;
            case Outline:
                drawOutline(generateBB(pos), outLine, lineWidth);
                break;
            case FillOutline:
                drawFill(generateBB(pos), fillColor);
                drawOutline(generateBB(pos), outLine, lineWidth);
                break;
            case Wireframe:
                drawWireframe(generateBB(pos), fillColor, lineWidth);
                break;
        }
        TessellatorUtil.release();
    }
    public static void drawBlock(AxisAlignedBB a, Color fillColor, Color outLine, float lineWidth, RenderMode mode,float scale ,float slab) {
        double f = 0.5 * (double) (1.0f - scale);
        AxisAlignedBB bb = RenderUtil.interpolateAxis(new AxisAlignedBB(a.minX + f, a.minY  + (double) slab, a.minZ + f, a.maxX - f, a.maxY - f, a.maxZ - f));
        TessellatorUtil.prepare();
        switch (mode) {
            case Fill:
                drawFill(bb, fillColor);
                break;
            case Outline:
                drawOutline(bb, outLine, lineWidth);
                break;
            case FillOutline:
                drawFill(bb, fillColor);
                drawOutline(bb, outLine, lineWidth);
                break;
            case Wireframe:
                drawWireframe(bb, fillColor, lineWidth);
                break;
        }
        TessellatorUtil.release();
    }
